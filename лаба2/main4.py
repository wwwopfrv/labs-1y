import math

a = float(input("введите значение a: "))
b = float(input("введите значение b: "))

print("что вы хотите узнать?")
choice = int(input("1 - среднее арифметическое, 2 - среднее геометрическое: "))

if choice == 1:
    print(f"Среднее арифметическое: {(a + b) / 2}")
elif choice == 2:
    if a >= 0 and b >= 0:
        print(f"Среднее геометрическое: {math.sqrt(a * b)}")
    else:
        print("Ошибка: для среднего геометрического оба числа должны быть неотрицательными")
else:
    print("Неверный ответ")